(function (window) {
    'use strict';

    var byId = window.MallowProducts || {};
    var list = Object.keys(byId).map(function (key) {
        return byId[key];
    }).filter(Boolean);

    var categories = {
        all: {
            id: 'all',
            label: 'Alles',
            note: 'Volledige collectie'
        },
        cremes: {
            id: 'cremes',
            label: 'Crèmes',
            note: 'Skincare'
        },
        zeepjes: {
            id: 'zeepjes',
            label: 'Zeepjes',
            note: 'Soap'
        },
        voeding: {
            id: 'voeding',
            label: 'Voeding',
            note: 'Food'
        },
        overig: {
            id: 'overig',
            label: 'Overig',
            note: 'Daily essentials'
        }
    };

    function findProduct(query) {
        if (!query) {
            return null;
        }

        var needle = String(query).trim().toLowerCase();
        if (!needle) {
            return null;
        }

        if (byId[needle]) {
            return byId[needle];
        }

        for (var i = 0; i < list.length; i += 1) {
            var product = list[i];
            if (!product) {
                continue;
            }

            var id = String(product.id || '').toLowerCase();
            var slug = String(product.slug || '').toLowerCase();
            if (needle === id || needle === slug) {
                return product;
            }
        }

        return null;
    }

    function toCartItem(product) {
        if (!product) {
            return null;
        }

        var id = String(product.id || product.slug || '');
        return {
            id: id,
            name: String(product.name || 'Product'),
            price: Number(product.price) || 0,
            image: String(product.image || ''),
            url: '/producten/' + encodeURIComponent(String(product.slug || id).toLowerCase())
        };
    }

    window.MallowCatalog = {
        byId: byId,
        list: list,
        categories: categories,
        find: findProduct,
        toCartItem: toCartItem
    };

    // Backward compatibility for older page scripts.
    window.productData = window.MallowProducts;
})(window);
